#include "libft.h"

int	ft_atoi(const char *str)
{
	int	n;
	int	k;

	n = 1;
	k = 0;
	while ((*str >= 7 && *str <= 14) || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			n *= -1;
		str++;
	}
	if (*str == '-' || *str == '+' || (*str >= 'A' && *str <= 'z'))
		return (0);
	while (*str >= 48 && *str <= 57)
	{
		k = (k * 10) + (*str - '0');
		str++;
	}
	return (k * n);
}
